<?php
//Найти в массиве число второе по величине
	
$a = [8,3,9,1,6,5,1,3];
$b = $a[0];
$c = $a[0];
$count = count ($a);
for ($x = 1; $x <= $count; $x++) { //находим самое большое число
	if ($b < $a[$x]) {
	$b = $a[$x];
	}
}
for ($y = 1; $y <= $count; $y++) {
	if ($c < $a[$y] and $a[$y] < $b) {
		$c = $a[$y];
	}
}
echo $c;
?>